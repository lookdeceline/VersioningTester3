import Foundation

public class Calculator {
    /// Adds the two numbers.
    public func add(_ first: Int, _ second: Int) -> Int {
        return first + second
    }
    
    public func multiply(_ first: Int, _ second: Int) -> Int {
        return first * second
    }
    
    public static var name = "Calculator"
    
    public static var name2 = "Calculator 2"
}
